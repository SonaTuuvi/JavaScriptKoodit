// Select the section element where cards will be added
const section = document.querySelector('#pictures');

// Select the <dialog> element for the image modal
const dialog = document.querySelector('dialog');

// Select the <img> inside the dialog for displaying the large image
const dialogImg = dialog.querySelector('img');

// Select the <span> used to close the dialog
const dialogClose = dialog.querySelector('span');

// Loop through each item in the picArray
picArray.forEach((item) => {
  // Create an <article> element to act as the card container
  const article = document.createElement('article');
  article.classList.add('card'); // Add 'card' class for styling

  // Create an <h2> element and set its text to the title
  const h2 = document.createElement('h2');
  h2.textContent = item.title;

  // Create a <figure> to contain the image and caption
  const figure = document.createElement('figure');
  const img = document.createElement('img');
  img.src = item.image.medium; // Set the image source to the medium version
  img.alt = item.title; // Set alt text for accessibility

  const figcaption = document.createElement('figcaption');
  figcaption.textContent = item.caption; // Set the image caption

  // Append the image and caption to the figure
  figure.appendChild(img);
  figure.appendChild(figcaption);

  // Create a <p> element and set its text to the description
  const p = document.createElement('p');
  p.textContent = item.description;

  // Append all parts to the article
  article.appendChild(h2);
  article.appendChild(figure);
  article.appendChild(p);

  // Add event listener to open the modal when the card is clicked
  article.addEventListener('click', () => {
    dialogImg.src = item.image.large; // Set large image source in modal
    dialogImg.alt = item.title;       // Set alt text
    dialog.showModal();               // Show the dialog (modal window)
  });

  // Append the article to the section on the page
  section.appendChild(article);
});

// Add event listener to close the modal when the <span> is clicked
dialogClose.addEventListener('click', () => {
  dialog.close(); // Close the dialog window
});
